public class Menu {
    private String num;
    private String name;
    private String detail;

    public String getNum() {
        return num;
    }
    public String getName() {
        return name;
    }
    public String getDetail() {
        return detail;
    }

    public Menu() {}

    public Menu(String num, String name, String detail) {
        this.num = num;
        this.name = name;
        this.detail = detail;
    }


}
